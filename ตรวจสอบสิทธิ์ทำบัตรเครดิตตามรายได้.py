income = int(input("กรอกรายได้ของคุณ (บาท): "))

if income < 15000:
    print("คุณยังไม่ผ่านเกณฑ์ขั้นต่ำในการสมัครบัตรเครดิต")
elif income < 30000:
    print("คุณไม่สามารถทำบัตรเครดิตได้")
elif 40000 <= income <= 89999:
    print("คุณสามารถสมัครบัตรเครดิตประเภท: เงิน")
elif 70000 <= income <= 99999:
    print("คุณสามารถสมัครบัตรเครดิตประเภท: ทอง")
elif income > 100000:
    print("คุณสามารถสมัครบัตรเครดิตประเภท: Platinum")
else:
    print("รายได้ของคุณไม่ตรงกับเงื่อนไขการสมัครบัตร")
